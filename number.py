# hash symbol is used for commenting in python
# 3 types of numbers in python-v3 int , float , complex
# in python-v3 size of an int has no size limits , no overflows

int_number = 123 # int
print('the number',int_number,'is a type of ', type(int_number))

float_number = 3.14 # float
print('the number',float_number,'is a type of ', type(float_number))

complex_num = 4 + 5j #complex number
print('the number',complex_num,'is a type of ', type(complex_num))

#python stores real and imaginary parts as floats
print('real part of the complex number',complex_num,'is', complex_num.real , 'imaginary part of the complex number is' , complex_num.imag  )

