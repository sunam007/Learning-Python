#Valid ways of writing strings

# Single quotation inside Double quotation
quote1 = "What's up ?"
print(quote1)

# Single quotation inside Single Quotation by using a back slash (\) before the apostrophe
quote4 = 'What\'s up ?'
print(quote4)

# Double quotation inside Single quotation
quote2 = 'He said - "I am free today" '
print(quote2)

# Both Double and single quotation inside a 3 double quotation (""" ... """) or 3 single quotation ('''...''')
quote3 = """Aristotle said - ""It's during our darkest moments that we must focus to see the light."" """
print( quote3 )